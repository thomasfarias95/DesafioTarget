package TerceiroDesafio;

public class Sequencia {

	 public static void main(String[] args) {
	        
	        int proximoA = 7 + 2;
	        System.out.println("Sequência a): " + proximoA);

	        
	        int proximoB = 64 * 2;
	        System.out.println("Sequência b): " + proximoB);

	        int proximoC = 6 * 6 + 1;
	        System.out.println("Sequência c): " + proximoC);

	        
	        int proximoD = 8 * 8;
	        System.out.println("Sequência d): " + proximoD);

	        
	        int proximoE = 8 + 5;
	        System.out.println("Sequência e): " + proximoE);

	       
	        int proximoF = 19 + 1;
	        System.out.println("Sequência f): " + proximoF);
	    }
}
